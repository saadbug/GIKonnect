export function getStudentName(regNo: string | number): string | null {
  const searchReg = String(regNo).trim();
  
  // Regex to find: S.No,RegNo,Name
  const regex = new RegExp(`^\\d+,${searchReg},([^,\\r\\n]+)`, "m");
  
  const match = RAW_CSV_DATA.match(regex);
  
  if (match && match[1]) {
    return toTitleCase(match[1].trim());
  }
  return null;
}

function toTitleCase(str: string) {
  return str.replace(
    /\w\S*/g,
    (txt) => txt.charAt(0).toUpperCase() + txt.substr(1).toLowerCase()
  );
}